    private void Search(String Keyword){
    SwingWorker<List<Station>,Void> searchWorker =  new SwingWorker<List<Station>,Void>(){
            @Override
            protected List<Station> doInBackground() throws Exception {
                StationController sc = new StationController();
                return sc.keywordSearch(Keyword);
            }
            @Override
            protected void done(){
                try {
                    List<Station> stations = get();
                    model.clear();
                  
                
                    for (Station station : stations){
                    model.addElement(station);
                    }
                    cbostations.setModel(model);
                } catch (InterruptedException ex) {
                    Logger.getLogger(frmstations.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ExecutionException ex) {
                    JOptionPane.showMessageDialog(frminitzone.this, ex.getMessage(), "Execution Exception", JOptionPane.ERROR_MESSAGE);
                }
            
            }
        
        };
    searchWorker.execute();
    }
