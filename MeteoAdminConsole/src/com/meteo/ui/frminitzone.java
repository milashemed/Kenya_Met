    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        btnsave.setEnabled(false);
        SwingWorker<List<Station>, Void> loadWorker = new SwingWorker<List<Station>,Void>(){
            @Override
            protected List<Station> doInBackground() throws Exception {
               StationController sc = new StationController();
               return sc.getItems();
            }
            @Override
            protected void done(){
                try {
                    List<Station> stations = get();
                    stations.stream().forEach((station) -> {
                       model.addElement(station);
                    });
                    cbostations.setModel(model);
                    cbostations.setSelectedIndex(0);
                    frminitzone.this.station = (Station)cbostations.getSelectedValue();
                    btnsave.setEnabled(true);
                } catch (InterruptedException ex) {
                    Logger.getLogger(frminitzone.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ExecutionException ex) {
JOptionPane.showMessageDialog(frminitzone.this, ex.getMessage(), "Execution Exception", JOptionPane.ERROR_MESSAGE);                }
                
            }
        
        };
        loadWorker.execute();
    }//GEN-LAST:event_formWindowOpened

