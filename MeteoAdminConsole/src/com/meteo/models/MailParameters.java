package com.meteo.models;

/**
 *
 * @author SALIM
 */
public class MailParameters {
    String auth;
    String startttls;
    String host;
    long port;
    String email;
    String password;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAuth() {
        return auth;
    }

    public void setAuth(String auth) {
        this.auth = auth;
    }

    public String getStartttls() {
        return startttls;
    }

    public void setStartttls(String startttls) {
        this.startttls = startttls;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public long getPort() {
        return port;
    }

    public void setPort(long port) {
        this.port = port;
    }
    
}
