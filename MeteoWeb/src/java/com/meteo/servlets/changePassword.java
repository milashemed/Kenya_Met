        response.setContentType("application/json");
        try(PrintWriter out = response.getWriter()) {
        String password = request.getParameter("newPassword");
        String username = request.getParameter("username");
       UserController usc = new UserController();
       User user = usc.getByUsername(username);
       String encpasswd = BCrypt.hashpw(password, BCrypt.gensalt());
       user.setPassword(encpasswd);
       user.setPasschange(0);
       usc.edit(user);
       Map<String,String> responseMap = new HashMap<>();
       responseMap.put("Status", "OK");
       out.println(Utils.JSONify(responseMap));
        }  
